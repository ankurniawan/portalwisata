                                    <h5>Jam Buka Sindu Kusuma Edupark</h5>    
                                    <p>
    Buka setiap hari pukul 08.00 - 23.00 WIB
    Sindu Kusuma Edupark (SKE) merupakan sebuah destinasi rekreasi yang terletak di Jogjakarta. Di tujuh hektar kawasan taman yang selesai dibangun tahun 2014 ini, terdapat bermacam-macam wahana, baik wahana bermain maupun wahana belajar yang bisa dinikmati oleh semua anggota keluarga.
    
    Wahana yang paling terkenal dari SKE adalah bianglalanya, yang merupakan sebuah wahana bermain sekaligus ikon SKE sendiri. Bila Singapore memiliki Flyer setinggi 165 meter, sementara Malaysia punya Eye setinggi 88 meter, dan di Thailand ada Asiatique setinggi 60 meter, Indonesia memiliki Cakra Manggilingan dengan tinggi 48 meter. Di tingkat Asia Tenggara, bianglala ini memang mesti berpuas diri duduk di peringkat keenam sebagai bianglala tertinggi, tetapi di tingkat nasional, ia duduk di peringkat pertama.</p>

                                    <blockquote class="firewall-blockquote mt-30 mb-30">
                                        <h5 class="mb-30">“Technology is nothing. What's important is that you have a faith in people, that they're basically good and smart, and if you give them tools, they'll do wonderful things with them.”</h5>
                                        <h6 class="text-muted">Steven Jobs</h6>
                                    </blockquote>
                                <p>erada di jantung Sindu Kusuma Edupark (SKE), Cakra yang terdiri dari 28 buah kapsul yang masing-masing dapat memuat empat orang termasuk ke dalam aspek bermain taman yang mengusung tagline "The City of Fantasy, Wahana Wisata Keluarga" itu. Daya tarik utama Cakra adalah persembahan bird eye view 360 derajat ketika kapsul yang kita tumpangi sudah berada di titik puncak.</p>
                                <p>Dari atas, kita tidak hanya menyaksikan pemandangan seantero SKE, atau wilayah sekitar yang rimbun hijau, atau Kota Jogja dan gedung-gedungnya, tetapi juga bukit-bukit di kejauhan dan Gunung Merapi. Dan bila senja-lalu kemudian malam-tiba, pemandangan semakin elok saja. Tidak heran bila wahana yang sekali berotasi memakan waktu enam menit ini menjadi ikon SKE. Cakra Manggilingan patut dicoba bila kita sedang berkunjung ke SKE.

Namun, SKE tidak terbatas pada Cakra. Saat kami berkunjung, ada setidaknya 18 wahana bermain yang sedang beroperasi, baik yang sudah lama ada maupun yang baru ditambahkan, beberapa diantaranya House of Terror (Rumah Hantu), Area Ketangkasan, Bioskop 8D, Bioskop 4D, Montor Tumbur (Bumper Car), Sepeda Mabur (Flying Bicycle), Cangkir Mabur (Spinning Cup), Kursi Mabur (Flying Chair), Taman Lampu, dan Waterpark. Dan semua wahana di SKE ini sebetulnya patut dicoba, walaupun hanya sekali.</p>
<p>Panggon Lunjak, misalnya. Di trampoline yang dilengkapi dengan mesin untuk membantu melontarkan pengunjung lebih tinggi ini, kita bisa melompat sesuka hati-bahkan sampai salto-salto-tanpa takut terpental keluar area. Wahana ini cukup menyenangkan, meskipun untuk alasan keselamatan, hanya mereka yang berberat badan maksimal 70 kg dapat mencobanya.Lalu, bioskop 4D dan 8D dengan pilihan-pilihan film yang cukup seru, mulai dari film kartun sampai film horror, juga siap menghibur kita. Yang pertama dinamai 4D Ride sebab selain gambar yang ditampilkan terlihat bervolume, juga karena kursi yang kita duduki bergerak mengikuti adegan di dalam layar dan ada efek hembusan angin, sehingga sensasi menonton terasa semakin nyata.Sementara itu, yang kedua, bioskop 8D, sebenarnya tidak jauh berbeda dari yang pertama. Bedanya hanya ada pada masalah tambahan efek-efek yang tidak ada di 4D. Misalnya, di bioskop 8D, semacam tuas keluar dari bagian bawah kursi dan mencolek-colek kaki kita, sehingga saat adegan-adegan menegangkan, kita bisa terperanjat kaget. Oleh karena itu, menonton film, khususnya bergenre horor di bioskop ini bakal seru.</p>
<p>Di samping wahana bermain, SKE juga menawarkan wahana edukasi. Begitu memasuki gerbang sebenarnya kita sudah merasakan sisi edukasi taman yang satu pengembang dengan Kusuma Agrowisata di Batu ini. Di koridor yang menuntun masuk diletakkan panel-panel informatif mengenai fakta-fakta yang mungkin kita belum ketahui sebelumnya. Misalnya, dijelaskan mengenai tinggi bianglala terbesar yang ada di dunia dibandingkan dengan kebanggan SKE sendiri.

Namun wahana edukasi SKE yang sebenarnya ada di dalam taman dan formatnya serupa museum. Yang pertama ada Omah Batik yang menyambut kita pertama kali begitu keluar dari koridor masuk. Di dalam museum batik ini, kita bisa mempelajari berbagai macam motif batik sekaligus mempelajari cerita perjalanan batik dari dulu sampai booming seperti sekarang.Kemudian ada juga Omah Musik yang menurut Yoga-Marketing dan Sales Supervisor SKE-menceritakan perkembangan sejarah musik. Di dalam museum ini pula kita juga bisa melihat macam-macam alat vintage yang berhubungan dengan musik. Contohnya, kita bisa melihat dan mendengar langsung gramofon antik yang dibunyikan dengan amplifier kayu yang juga antik memutar lagu lawas Indonesia yang tidak lagi terdengar di televisi.Dengan jumlah hanya dua, wahana edukasi SKE memang masih terbilang sedikit. Namun, berdasarkan keterangan Yoga, dalam waktu dekat wahana edukasi lain akan segera menyusul, di samping wahana bermain baru juga. Tiga contoh yang disebutkan pria kelahiran Bangka yang sore itu mengantarkan kami berkeliling SKE adalah planetarium dan permainan menggambar virtual untuk wisata belajar, dan water park besar untuk kelompok wisata bermain.

Di luar ini, Yoga juga menambahkan bahwa dalam waktu dekat, halaman parkir SKE yang amat luas akan ditransformasi sebagian menjadi pasar kuliner yang bakal menarik perhatian muda-mudi atau siapa saja yang ingin berwisata kuliner di malam hari. Untuk saat ini, bila ingin makan sambil bersantai setelah lelah bermain di SKE, ada Jogja Festival Resto dan kafe yang menawarkan berbagai makanan dan minuman.</p>   
                                    
