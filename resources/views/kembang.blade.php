                                    <p>Selain itu pada saat musim tanam padi tiba, para pengunjung juga dapat menikmati edukasi dan terlibat langsung dalam kegiatan mengolah sawah seperti membajak sawah dan menanam padi. Pengalaman bercocok tanam langsung dari sawah dapat menjadikan pengalaman yang unik dan menarik terutama bagi generasi muda, terutama untuk menghargai makanan yang telah tersedia di atas meja.</p>
                                

