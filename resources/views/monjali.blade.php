                                        <h2 class="post-headline">Museum Monjali Saksi Sejarah di Yogyakarta</h2>
                                    </a>

                                   <p>Bangunan Berbentuk Seperti Tumpeng Dengan Taman Lampion

Kota Yogyakarta juga menyimpan perjuangan selama masa penjajahan Belanda. Semuanya ada di sebuah museum yang letaknya tidak jauh dari pusat kota Yogyakarta. Nama museum tersebut adalah Museum Monumen Jogja Kembali. Tepatnya di Dusun Jongkang, Sariharjo, Kec. Sleman, Daerah Istimewa Yogyakarta. Monjali (Monumen Jogja Kembali) disebut-sebut sebagai wisata keluarga yang memiliki nilai-nilai edukasi sekaligus wisata yang menyenangkan.</p>

                                    <blockquote class="firewall-blockquote mt-30 mb-30">
                                        <h5 class="mb-30">“Technology is nothing. What's important is that you have a faith in people, that they're basically good and smart, and if you give them tools, they'll do wonderful things with them.”</h5>
                                        <h6 class="text-muted">Steven Jobs</h6>
                                    </blockquote>

                                <p>Selain terdapat sebuah taman bernama Taman Pelangi (sebagian orang menyebutnya Taman Lampion), di obyek wisata Monjali ini juga terdapat museum sejarah dan budaya. Museum Monumen Jogja Kembali merupakan penghormatan kepada pahlawan-pahlawan yang merebut kembali kemerdekaan Republik Indonesia dari tangan Penjajah Belanda. Tgl 1 Maret 1949 pasukan TNI yang dipimpin oleh Letkol Soeharto atas persetujuan Sri Sultan Hamengkubuwono IX melakukan serangan dadakan ke markas Belanda di Pusat Kota Yogyakarta.

Monumen ini dibangun dengan bentuk kerucut seperti tumpeng, sebenarnya bentuk tersebut menyerupai gunung. Bangunan ini sebagai pertanda kesuburan dan sebagai upaya pelestarian budaya nenek moyang. Penempatan Museum Monumen Jogya Kembali (Monjali) bukan tanpa maksud. Letak bangunannya terletak pada sumbu imajiner yang menghubungkan antara Merapi, keraton, Tugu, Panggung Krapyak, dan parangtritis. Titik imajiner tepatnya pada lantai tiga menumen tersebut, tepat berdirinya tiang bendera.

Museum ini sering di kenal oleh masyarakat sekitar dengan sebutan Monjali yaitu singkatan dari Monumen Jogja Kembali, luas lahan dari museum ini sendiri cukup luas, diperkirakan luasnya mencapai 5 Hektar. Selain bentuk monumen yang unik, penataan Museum Monumen Jogja Kembali (monjali) juga menarik. Terdapat empat jalan menuju bangunan utama yang dikelilingi kolam (jagang). Museum Monumen Jogja Kembali ini memiliki 4 lantai secara keseluruhan didalamnya terdapat benda-benda bersejarah dan klasik diantaranya adalah mesin ketik jaman dahulu, telephone, buku – buku ensiklopedia, Senjata, replika dan masih banyak lagi.</p>

                                <p>Saat memasuki halaman museum, dimana terdapat sebuah dinding besar yang memuat sebuah puisi Chairil Anwar berjudul “Karawang-Bekasi” dan 422 nama pahlawan yang telah gugur di daerah Wehrkreise III tanggal 19 Desember 1948 sampai 29 Juni 1949. Pada lantai dasar terdapat beberapa benda bukti sejarah perjuangan bangsa Indonesia melawan penjajah dan juga beberapa benda klasik yang cukup unik seperti telpon jaman dulu, mesin ketik, beragam senjata dan masih banyak lagi.

Lantai kedua, terdapat beberapa diorama atau patung sebuah patung lengkap dengan background dan pakaianya menggambarkan perjuangan ketika pada zaman penjajahan Belanda yang mengkhianati perjanjian Reville dan membuat pemerintah marah dan terjadi pertumpahan darah, selain itu di lantai dua ini terdapat lukisan dinding yang menggambarkan perjuangan bangsa Indonesia pada waktu itu dengan 2 Dimensi. Lantai ke tiga, pada ruangan ini dikhususkan ruangan untuk mendoakan para pahlawan yang telah berjuang dan gugur, tempat ini dinamakan sebagai tempat Garbha Graha, pada bagian tengah ruangan ini terdapat bendera merah putih berkibar yang melambangkan kemerdekaan Indonesia yang di belakangnya terdapat lukisan tulisan Mantan Presiden ke 2 RI yaitu Alm. Pak Suharto.

Harga tiket masuk obyek wisata Monjali ini adalah Rp 10.000 untuk hari biasa dan Rp 15.000 untuk hari libur atau hari-hari besar lainnya. Dengan satu tiket saja Anda bisa mengelilingi komplek wisata di tempat ini. Untuk membawa kamera DSLR sendiri dikenakan biaya Rp. 1,000. Taman wisata yang terdapat di Monjali ini bisa Anda nikmati dari pagi hingga malam, dimana Anda akan dimanjakan dengan sejumlah fasilitas permainannya yang begitu mengasikan. Terdapat lebih dari 20 jenis permainan, 25 stand makanan dan minuman yang bisa menambah kegembiraan.</p>

                                <p>Taman yang dibuka tahun 2011 ini memiliki fasilitas mulai dari wahana atraksi maupun wahana air, sepeti euro bungae, rumah ballon, battery car, junior jet, trampoline, halicak,  bumper boat, speed boat, bom bom car, sepeda tandem, safari train, ATV, becak air,  seperti bola air, perahu dayung dan  lainnya sebagainya. Harga tiap wahana bervariasai, mulai dari Rp 10.000 sampai Rp 20.000. Bagi Anda yang hobi memancing, Anda bisa menikmati fasilitas kolam pemancingan yang ada di taman ini, dan yang menarik adalah bisa langsung dimasak.

Monumen Jogja Kembali (Monjali) pada malam hari suasana berubah menjadi berwarna. Di sana banyak lampu lampion dengan berbagai bentuk. Berbeda dengan siang hari, wisata malam di Monjali juga sangat menarik. Anda bisa menikmati berbagai wahana seperti sepeda dan kendaraan dengan hiasan lampu yang menarik. Berbagai lampion dengan bentuk dan warna yang indah mengelilingi Monjali. Selain itu, ada juga wahana balon air besar yang dapat memuat dua atau tiga orang dan balon ini akan bergerak menggelinding di atas permukaan air.

Bagi Anda yang ingin berkunjung ke wisata Monumen Jogja Kembali tidak perlu khawatir dan repot membawa bekal dari rumah karena di kawasan wisata ini terdapat banyak pedagang yang menjual beragam panganan. Mulai dari yang hanya sekedar jajanan cemilan ringan, makanan berat hingga beragam pilihan minumanpun tersedia disini. Soal harga, Anda tidak perlu khawatir karena harga yang ditawarkan pun sangat terjangkau.

Museum Monumen Jogja Kembali adalah kebanggaan warga Yogyakarta yang wajib anda kunjungi. Sebagai bagian wisata sejarah yang akan memperkaya pengetahuan, tempat ini cukup menarik minat wisatawan termasuk dari mancanegara. Monumen Yogya Kembali ini bisa di akses dari perempatan Tugu Yogyakarta lurus ke arah utara sampai ke perempatan ringroad utara. Setelah sampai di perempatan ringroad utara belok ke kiri (arah barat), kemudian ambil jalur ke arah sebaliknya melalui area pemotong arah yang sudah disediakan. Monumen Jogja Kembali berada sekitar 50 meter sebelah barat perempatan ringroad pada sisi utara jalan.</p>
                                                                      
