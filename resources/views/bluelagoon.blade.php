                                        <h2 class="post-headline">Wisata Alam Blue Lagoon</h2>
                                    </a>
                                    <p>Blue Lagoon Jogja merupakan salah satu obyek wisata yang di jadikan tempat pemandian sumber mata air ini yang muncul dari permukaan tanah di dalam kolam. Apabila Anda melihat ke dalam kolam akan terlihat gelembung-gelembung kecil yang muncul dari permukaan tanah. Hal itu menunjukkan bahwa terdapat air yang terus menerus muncul dari permukaan tanah tersebut. Meskipun Blue Lagoon di Jogja ini sudah lama namun tempat wisata ini mulai tenar dipertengahan tahun 2014. Pemandian Blue Lagoon ini terletak di Desa Dalem, Widodomartani, Ngemplak, Sleman, Yogyakarta.

Menurut salah satu warga, tempat ini juga dikenal dengan nama Tirta Budi. Tempat ini ramai pengunjung pada saat hari sabtu, minggu dan juga hari libur lainnya. Biasanya Blue Lagoon ini kebanyakan dikunjungi oleh anak-anak muda yang ingin berenang, foto-foto atau sekedar bermain air. Apabila Anda ingin berkunjung ke tempat ini, sebaiknya Anda berpakaian santai dan menyediakan baju cadangan karena tempat ini merupakan wisata air.</p>

                                    <blockquote class="firewall-blockquote mt-30 mb-30">
                                        <h5 class="mb-30">“Technology is nothing. What's important is that you have a faith in people, that they're basically good and smart, and if you give them tools, they'll do wonderful things with them.”</h5>
                                        <h6 class="text-muted">Steven Jobs</h6>
                                    </blockquote>

                                <p>Untuk bisa menikmati segarnya air alami di sini tentu saja para anda akan diharuskan membayar sejumlah harga tiket masuk Blue Lagoon Jogja. Tapi jangan khawatir, karena harga tiket masuk Blue Lagoon Jogja yang harus dibayarkan tidaklah mahal. Anda cukup merogoh kocek sedalam Rp 3.000,- saja untuk setiap orangnya. Sedangkan jika anda membawa kendaraan roda dua, untuk ongkos parkirnya anda cukup membayar Rp 2.000,-

Bagi anda yang berkunjung ke Blue Lagoon Jogja ini anda tidak perlu khawatir dengan fasilitas yang ada. Tempat ini juga telah didukung dengan beberapa fasilitas umum yang bisa digunakan untuk menunjang kenyamanan para pengunjung. Misalnya toilet umum, ruang ganti pakaian, bahkan juga beberapa warung makan kecil juga telah tersedia di area ini. Jadi selain berenang anda juga bisa menikmati suasana alam yang asri sambil minum kopi atau sekadar duduk-duduk ngobrol dengan santai.

Akses menuju tempat ini tergolong mudah. Dari pusat Kota Jogja, wisatawan cukup menyusuri Jalan Kaliurang. Sesampainya di pertigaan Besi – Jangkang (Jalan Kaliurang Km 13), silahkan belok kanan dan ikuti jalan raya hingga Pasar Jangkang. Dari pertigaan Pasar Jangkang ambil arah kanan sekitar 100 meter dan ikuti petunjuk arah yang menuju Blue Lagoon.</p>
                                  
