                                <h5>KOMPLEKS CANDI PRAMBANAN</h5>
                                <p>
Diperkirakan ada 240 candi besar dan kecil di kompleks Candi Prambanan. Namun, hanya 18 candi yang berhasil dipugar, sisanya adalah tumpukan batu yang berserakan.

Tiga dari 8 candi utama disebut candi Trimurti ("tiga wujud"), dipersembahkan untuk 3 dewa Hindu tertinggi: Dewa Brahma Sang Pencipta, Wisnu Sang Pemelihara, dan Siwa Sang Pemusnah.Candi Siwa yang berada di tengah-tengah adalah bangunan terbesar di kompleks Candi Prambanan dan memiliki 5 ruangan: timur, selatan, barat, utara, dan sebuah ruangan utama di tengah candi. Ruangan timur terhubung dengan ruangan utama yang berisi arca Siwa Mahadewa setinggi 3 meter. Ruangan utara berisi arca Durga Mahisasuramardini, istri Siwa. Arca Durga inilah yang disebut sebagai Roro Jonggrang dalam legenda setempat.Tepat di depan candi Trimurti terdapat 3 candi yang lebih kecil untuk kendaraan atau wahana dewa-dewa ini; sang lembu Nandi wahana Siwa, sang Angsa wahana Brahma, dan sang Garuda wahana Wisnu.</p>                                       
                            <h5>RELIEF CERITA RAMAYANA</h5>
                                <p>
Candi Prambanan dihiasi relief yang menceritakan kisah Ramayana dan Krishnayana. Kisah Ramayana dimulai dari kelahiran Rama, penculikan Sinta (istri Rama) oleh Rahwana, upaya penyelamatan Sinta oleh Hanoman (panglima kera), pertemuan kembali Rama dan Sinta, hingga penyerahan tahta Rama kepada anaknya.

Relief cerita Ramayana ini dipahat pada dinding sebelah dalam pagar sepanjang lorong galeri yang mengelilingi candi utama. Relief ini dibaca dengan cara mengitari candi searah jarum jam, dimulai dari sisi timur candi Siwa (42 adegan) dan dilanjutkan ke candi Brahma (30 adegan).

Tak jauh dari Candi Prambanan, kisah cinta Rama dan Sinta yang terpahat abadi ini juga dipentaskan dalam bentuk sendratari setiap Selasa malam, Kamis malam, dan Sabtu malam. Sendratari Ramayana Prambanan memegang rekor dunia Guinness World Records sebagai pentas tari Ramayana yang paling banyak melibatkan penari sekaligus sebagai sendratari yang paling lama dan rutin mementaskan tari Ramayana sejak 1961 hingga 2012. London dan New York memiliki The Phantom of the Opera, Jogja punya Sendratari Ramayana. Pada musim kemarau, pementasan dilakukan di luar ruangan dengan Candi Prambanan yang disorot lampu sebagai latar belakangnya.</p>

