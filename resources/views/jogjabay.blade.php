                                        <h2 class="post-headline">Jogjabay</h2>
                                    </a>
                                  <p>Liburan adalah sesuatu yang sangat menyenangkan tetapi, Sobat Native mungkin bingung mau liburan kemana? Udara panas memang paling tepat untuk berenang dan bermain air. Nah, permainan air ini menjadi sesuatu hal yang paling menyenangkan. Di Yogyakata, ada banyak sekali kawasan yang bisa dipakai untuk berenang.</p>

                                    

                                   <p>Diantara semua tempat tersebut, ada satu tempat yang membuat semua mata akan tertuju ke tempat ini. Jogja Bay Pirates Adventure Waterpark adalah tempatnya bermain air. Destinasi kelas dunia dan menjadi waterpark terbesar di Indonesa dan Asia Tenggara ini menyimpan segala keseruan dan tantangan menarik.</p>
                                <h5>Mengenal Jogja Bay</h5>
                                   <p>
Kawasan ini dibangun diatas tanah milik kas daerah maguwoharjo. Dengan luas kurang lebih 7,7 ha. Kurang lebih ada 19 wahana permainan yang bisa dinikmati oleh Sobat Native. Banyak wisatawan berkata bila Jogja bay adalah waterpark tercanggih dan terlengkap di Indonesia dengan Tema Bajak laut.

Pihak pengelola Jogja Bay memadukan dua buah konsep antara tradisi jogja dan tokoh bajak laut eropa yang asyik dan menyenangkan. Sebuah perkampungan Bajak laut yang dikemas dengan penuh tanaman hijau dan thematic old pirates.

Cukup menyenangkan bukan berada di kawasan ini?</p>
                                <h5>Wahana Jogja Bay</h5>
                                <p>
Wahana Jogja Bay ini dikemas secara khusus untuk permainan dan edukasi. Dimana, Sobat Native akan disuguhkan dengan wahana yang berpadu dengan gempa bumi dan juga tsunami. Ada sedikitnya 8 ombak besar yang tersedia disini. Sobat Native, bisa belajar bagaimana caranya menghadapi gempa bumi dan tsunami.Jogja Bay juga menghadirkan beberapa ikon karnaval yang bisa disaksikan pertunjukkannya setiap hari sabtu dan minggu di Harbour Teater. Pertunjukan Spektakuler ini akan dimulai pada pukul 3 sore. Sobat Native pun juga bisa berfoto-foto ria dengan karakter-karakter tersebut. Drama musikal ini menceritakan tentang jogja bay dengan judul The Guardian Past In The Future.Nah, di tempat ini Sobat Native bisa menkmati museum air water for life. Museum ini akan membahas secara tuntas bagaimana air yang berfungsi untuk tubuh. Selain itu, ada pula sejarah panjang mengenai selokan mataram, tempat pengaliran air terbesar di Yogyakarta dan namanya sudah dikenal oleh seluruh warga Yogyakarta.

Memo racer, wahana di Jogja Bay yang mungkin bisa dijadikan sebagai tempat untuk mengadu kecepatan. Sobat Native bisa meluncur dan menimati keseruan ini bersama dengan sahabat atau dengan keluarga, siapa cepat dia adalah pemenangnya. Menarik bukan? Ayo meluncur dan buktikan jika kamu adalah sang pemenang.</p>
                                <p>Kelihatannya tidak afdol bila tidak berkeliling area. Oleh sebab itu, wahana bekti advanture pun dibuat. Memenuhi keinginan Sobat Native dimana, bekti adalah pemimpin pasukan keraton yang akan mengajak berkeliling dengan menggunakan papan seluncur. Meliuk, berkelok dan berputar menjadi sajian utama yang menyenangkan.

Ingin berteriak dan melpaskan segala beban penat di dalam tubuh. Sobat Native bisa menikmati wahana Volcano Coaster. Dimana, Sobat Native akan bermuara di sungai Jogja Bay. Jangan malu untuk berteriak sekencang-kencangnya ya Sobat Natïve.

</p>
                                <p>Timo-timo rider wahana yang memiliki lintasan tidak terlalu panjang memang tetapi, jangan salah, jangan tergoda dengan tampaknya disini, Sobat Native bisa merasakan bagaimana rasanya terombang-ambing di dalam lautan lepas.

Jolie Raft River. Nikmati santai seperti berada di pantai dimana Sobat Natïve diwajibkan untuk mengikuti arus air yang sangat menyenangkan. Mau merasakan menjadi boomerang? Ada wahana yang bernama Brando Bomeranggo. Sobat Native akan akan meluncur di papan luncur dengan kecepatan tinggi, dan rasakan menjadi boomerang di akhir lintasan.

Ziggy Giant Barrel, wahana ini mempunyai papan luncur yang tidak terlalu besar. Tetapi, Sobat Native akan mendapatkan sebuah kejutan dari bazel besar yang berada di atas wahana. Jangan terkejut dengan guyuran air ini ya Sobat Native.

Ingin merasakan bagaimana menghadapi tsunami? Ada wahana bernama South Beach dimana Sobat Natie bisa merasakan ombak-omak besar layaknya tsunami yang datang sewaktu-watu menghantam. Jadi, siap-siap ya Sobat Native.

Kula Playpool waktunya untuk bersantai dengan adanya pool bar di sisi kolam. Menyenangkan bukan? Nah, mau mengalir bersama dengan air Sobat native bisa memanfaatkan wahana Donte wild river.

Ingin bersantai bersama dengan keluarga, Sobat Native bisa memanfaatkan wahana Mimi Family. Ingin bebas bermain air atau cukup beristirahat di gazebo, Sobat Native bisa berkunjung ke HIP Playground.</p>
